async function run() {
  // Найти оба курса
  const courses = await client.fetch(`*[_type == "course"]{ _id, title, slug }`)
  console.log('Курсы в базе:')
  courses.forEach(c => console.log(`  [${c._id}] ${c.title} (${c.slug?.current})`))

  // Найти клуб
  const klub = courses.find(c =>
    c.title?.toLowerCase().includes('клуб') ||
    c.slug?.current?.includes('klub') ||
    c.slug?.current?.includes('club')
  )

  // Найти сакральную
  const sakral = courses.find(c =>
    c.title?.toLowerCase().includes('сакрал') ||
    c.slug?.current?.includes('sakral')
  )

  if (klub) {
    await client.patch(klub._id).set({ benefits: klubBenefits }).commit()
    console.log(`✓ Обновлён: ${klub.title}`)
  } else {
    console.log('✗ Клуб не найден')
  }

  if (sakral) {
    await client.patch(sakral._id).set({ benefits: sakralBenefits }).commit()
    console.log(`✓ Обновлён: ${sakral.title}`)
  } else {
    console.log('✗ Сакральная не найдена')
  }
}

run().catch(console.error)
